package day58_자바정리_수1_저번주_문제_추가요구;

public class URLParameter {
	
		public String key;
		public String value;
		
		public URLParameter() 
		{

			key = null;
			value = null;
		}
		
		public URLParameter(String key, String value) 
		{
			this.key  = key;
			this.value = value;
		}
		
	
}
