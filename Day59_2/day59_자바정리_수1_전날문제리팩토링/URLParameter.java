package day59_자바정리_수1_전날문제리팩토링;

public class URLParameter {
	
		public String key;
		public String value;
		
		public URLParameter() 
		{

			key = null;
			value = null;
		}
		
		public URLParameter(String key, String value) 
		{
			this.key  = key;
			this.value = value;
		}
		
	
}
